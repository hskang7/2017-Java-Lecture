class ArithmeticExample {
	public static void main(String args[]) {
		int x, y, u, w, z;
		x = 5;
		y = 3;
		u = 8;
		w = 10;
		
		System.out.println("x 값은:" + x);
		System.out.println("y 값은:" + y);
		System.out.println("u 값은:" + u);
		System.out.println("w 값은:" + w);
		
		z = x + y;
		System.out.println("x + y 값은:" + z);
		z = u - w;
		System.out.println("u - w 값은:" + z);
		z = x * u;
		System.out.println("x * u 값은:" + z);
		z =  w / x;
		System.out.println("w / x 값은:" + z);
		}
}
